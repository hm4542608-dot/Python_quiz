# Exercise 1:
# Ask the user to enter the name of their city.
# Then print how many characters are in the city's name using len().
city_name = input("Please enter your city name: ")
city_length = len(city_name)
print("Characters in your city name", city_length)
# Exercise 2:
# Ask the user to write the name of any Islamic scholar they admire.
# Then print how many letters are in that name.
scholar_name = input("Please enter the name of your favorite scholar name: ")
scholar_length = len(scholar_name)
print("Characters in your favorite scholar name" , scholar_length)
